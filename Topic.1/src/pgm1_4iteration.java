// program to illustrate iteration
public class pgm1_4iteration {

	public static void main(String[] args) {
		//declare some variables
		int number, sqnumber;
		
		//initialize number, calculate its square, and print the results
		number = 4;
		sqnumber = number * number;
		System.out.println("number = " + number + " sqnumber = " + sqnumber);

		//change the value of number, recalculate its square, and print the results
		number = 5;
		sqnumber = number * number;
		System.out.println("number = " + number + " sqnumber = " + sqnumber);

		//change the value of number, recalculate its square, and print the results
		number = 6;
		sqnumber = number * number;
		System.out.println("number = " + number + " sqnumber = " + sqnumber);

		//change the value of number, recalculate its square, and print the results
		number = 7;
		sqnumber = number * number;
		System.out.println("number = " + number + " sqnumber = " + sqnumber);

		//change the value of number, recalculate its square, and print the results
		number = 8;
		sqnumber = number * number;
		System.out.println("number = " + number + " sqnumber = " + sqnumber);

	}

}